package ct.af.message.incoming.parameter;

public class SDFSidNotificationElement {
	
	private String dn;
	private String objectClass;
	private String priority;
	private String sidNumRepetitions;
	private String sidValidityPeriod;
	private String sidSMServiceType;
	private String sidMessagingMode;
	private String sidNotificationInfo;
	private String sidTextMessage;
	private String sidTextUnicode;
	
	
	public String getDn() {
		return dn;
	}
	public void setDn(String dn) {
		this.dn = dn;
	}
	public String getObjectClass() {
		return objectClass;
	}
	public void setObjectClass(String objectClass) {
		this.objectClass = objectClass;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getSidNumRepetitions() {
		return sidNumRepetitions;
	}
	public void setSidNumRepetitions(String sidNumRepetitions) {
		this.sidNumRepetitions = sidNumRepetitions;
	}
	public String getSidValidityPeriod() {
		return sidValidityPeriod;
	}
	public void setSidValidityPeriod(String sidValidityPeriod) {
		this.sidValidityPeriod = sidValidityPeriod;
	}
	public String getSidSMServiceType() {
		return sidSMServiceType;
	}
	public void setSidSMServiceType(String sidSMServiceType) {
		this.sidSMServiceType = sidSMServiceType;
	}
	public String getSidMessagingMode() {
		return sidMessagingMode;
	}
	public void setSidMessagingMode(String sidMessagingMode) {
		this.sidMessagingMode = sidMessagingMode;
	}
	public String getSidNotificationInfo() {
		return sidNotificationInfo;
	}
	public void setSidNotificationInfo(String sidNotificationInfo) {
		this.sidNotificationInfo = sidNotificationInfo;
	}
	public String getSidTextMessage() {
		return sidTextMessage;
	}
	public void setSidTextMessage(String sidTextMessage) {
		this.sidTextMessage = sidTextMessage;
	}
	public String getSidTextUnicode() {
		return sidTextUnicode;
	}
	public void setSidTextUnicode(String sidTextUnicode) {
		this.sidTextUnicode = sidTextUnicode;
	}
	
	

}
